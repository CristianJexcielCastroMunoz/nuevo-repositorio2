# Pedimos al usuario que ingrese su nombre
name = input("¿Cómo te llamas? ")

# Convertimos el nombre a minúsculas y lo mostramos
print(name.lower())

# Convertimos el nombre a mayúsculas y lo mostramos
print(name.upper())

# Convertimos el nombre a formato de título (primera letra en mayúscula) y lo mostramos
print(name.title())


